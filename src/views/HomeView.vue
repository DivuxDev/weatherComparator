    console.log("MeteoSource Response:", data);
    weatherMeteoSourceResult.value = data;
  } catch (error) {
    console.error("Error al obtener datos de MeteoSource:", error);
  }
}

async function fetchOpenMeteoApi() {
  const now = new Date();
  const formatDate = (date ) => date.toISOString().split('T')[0];
  const startDate = new Date();
  startDate.setDate(now.getDate()  );

  const endDate = new Date();
  endDate.setDate(now.getDate() + 5 );


  const url = `${apiUrls.apiOpenMeteo}?latitude=${lat.value}&longitude=${lon.value}&start_date=${formatDate(startDate)}&end_date=${formatDate(endDate)}`
  try {
    const response = await fetch(url);
    if (!response) {
      throw new Error(`Error en la solicitud: ${response}`);
    }

    const data = await response.json();
    console.log("OpenMeteo Response:", data);
    weatherMeteoSourceResult.value = data;
  } catch (error) {
    console.error("Error al obtener datos de OpenMeteo:", error);
  }
}



