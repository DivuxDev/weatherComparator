};
const days = 7;
const lat = ref<string>("");
const lon = ref<string>("");

async function fetchWeatherbit() {
  const url = `${apiUrls.apiWeatherBit}/daily?lat=${lat.value}&lon=${lon.value}&days=${days}&key=${apiKeys.apiKeyWeatherBit}&lang=en&units=M`;
  try {
    const response = await fetch(url);
    if (!response) {
      throw new Error(`Error en la solicitud: ${response}`);
    }

    const data = await response.json();
    console.log("Weatherbit Response:", data);

    weatherbitResult.value = data;
  } catch (error) {
    console.error("Error al obtener datos de Weatherbit:", error);
  }
}

async function fetchWeatherAPI() {
  const url = `${apiUrls.apiWeatherApi}/forecast.json?key=${apiKeys.apiKeyWeatherApiKey}&q=${lat.value},${lon.value}&days=${days}&lang=en`;
  try {
    const response = await fetch(url);
    if (!response) {
      throw new Error(`Error en la solicitud: ${response}`);
    }

