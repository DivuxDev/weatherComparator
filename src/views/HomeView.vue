async function fetchMeteoSourceApi() {
  const url = `${apiUrls.apiMeteosource}/point?lat=${lat.value}&lon=${lon.value}&sections=all&timezone=UTC&language=en&units=metric&key=${apiKeys.apiKeyMeteosource}`
  try {
    const response = await fetch(url);
    if (!response) {
      throw new Error(`Error en la solicitud: ${response}`);
    }

